void menuProduto(void) {
    char opcao;

    do {
        opcao = tela_menu_produto();
        switch(opcao) {
            case '1': 	tela_cadastrar_produto();
                        break;
            case '2': 	tela_pesquisar_produto();
                        break;
            case '3': 	tela_alterar_produto();
                        break;
            case '4': 	tela_excluir_produto();
                        break;
        } 		
    } while (opcao != '0');
}


